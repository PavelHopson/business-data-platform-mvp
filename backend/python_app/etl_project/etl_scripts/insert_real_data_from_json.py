def insert_real_data():
    db: Session = SessionLocal()

    for item in json_data:
        inn = item.get('ИНН')
        ogrn = item.get('ОГРН')
        name = item.get('НаимПолнЮЛ') or item.get('НаимСокрЮЛ')
        status = item.get('Статус')
        address = item.get('Адрес', {}).get('АдресПолн')
        reg_date_str = item.get('ДатаРег')

        reg_date = None
        if reg_date_str:
            try:
                reg_date = datetime.strptime(reg_date_str, '%Y-%m-%d').date()
            except ValueError:
                print(f"⚠️ Неверный формат даты: {reg_date_str}")

        company = Company(
            inn=inn,
            ogrn=ogrn,
            name=name,
            status=status,
            address=address,
            registration_date=reg_date
        )
        db.add(company)
        db.flush()

        founders_data = item.get('Учредители', [])
        for f in founders_data:
            founder_name = f.get('Наим')
            share = f.get('Доля', {}).get('Процент')

            if not founder_name:
                continue

            founder = Founder(
                company_id=company.id,
                name=founder_name,
                share=share
            )
            db.add(founder)

        financial = Financial(
            company_id=company.id,
            year=2023,
            revenue=1000000.0,
            profit=500000.0,
            assets=2000000.0
        )
        db.add(financial)

        court_case = CourtCase(
            company_id=company.id,
            case_number="12345/2023",
            date=datetime.now().date(),
            status="Рассматривается",
            type="Арбитраж"
        )
        db.add(court_case)

        contract = Contract(
            company_id=company.id,
            customer="Тестовый заказчик",
            amount=500000.0,
            date=datetime.now().date()
        )
        db.add(contract)

    try:
        db.commit()
        print("✅ Реальные данные успешно вставлены")
    except Exception as e:
        print(f"❌ Ошибка транзакции: {e}")
        db.rollback()
    finally:
        db.close()

if __name__ == "__main__":
    insert_real_data()